		<h2>Competitive Programming</h2>
		
		<br>
		<p class="competition-another-content">
			Competitive Programming merupakan kompetisi untuk menguji kemampuan sistematika dan nalar peserta dengan memberikan persoalan yang harus dipecahkan menggunakan bahasa 
