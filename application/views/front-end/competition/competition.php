		<h2>Robotics Competition</h2>
		
		<br>
		<p class="competition-another-content">
			Robotics Competition merupakan kompetisi yang bertujuan untuk menstimulus semangat belajar generasi muda bangsa terhadap IT, khususnya robotika, dengan cara yang 
			menyenangkan. Kompetisi ini terdiri dari dua cabang kompetisi, yaitu: kompetisi robot <i>maze-solving</i> untuk tingkat SMA/sederajat (tim, maksimal 3 orang/tim) dan 
			kompetisi robot <i>path-finding</i> untuk tingkat SMP/sederajat (tim, maksimal 2 orang/tim). Pada cabang kompetisi robot <i>maze-solving</i>, robot yang digunakan adalah robot 
			yang dapat membaca lintasan/garis hitam diatas putih, robot dapat berupa robot buatan sendiri ataupun rakitan (robokit) yang dibawa peserta. Peserta akan merakit 
			dan memprogram robot tersebut sehingga dapat menyelesaikan <i>maze</i> yang diberikan untuk tiap babak. Cabang kompetisi ini akan terdiri dari babak penyisihan dan babak final. 
			Cabang kompetisi robot <i>path-finding</i> untuk SMP akan diselanggarakan atas kerjasama dengan KlubRobotik. Cabang ini akan menggunakan konsep perlombaan IMAGINE-Rule, 
			dimana peserta akan menggunakan robot <b>Fischertechnik Robo Mobile Set</b> yang sudah disediakan panitia dan sebuah software <b>RoboPro</b> untuk menentukan <i>path</i> yang paling 
			optimal untuk dilalui dari maze yang diberikan agar mendapatkan poin setinggi mungkin sebelum tiba di garis <i>finish</i>. 
		</p>
		
