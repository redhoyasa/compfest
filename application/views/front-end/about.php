	<h2>Apa itu CompFest?</h2>
<p class="buh" >
	CompFest merupakan <i>one-stop IT event</i> tahunan yang diselenggarakan oleh mahasiswa Fakultas Ilmu Komputer Universitas Indonesia yang terdiri atas rangkaian
	kompetisi, <i>roadshow</i>, seminar, <i>playground</i>, dan <i>entertainment</i>.
</p>
<br/>
<p class="buh" >
	CompFest 2013 memiliki tema "Facing National Development Towards Innovation and Collaboration - FANTASTIC" yang bertujuan untuk memacu dan meningkatkan
	kolaborasi antar akademisi, lembaga pemerintahan, perusahaan, <i>start-up</i>, <i>developer</i>, dan komunitas agar mendapatkan hasil inovasi terbaik untuk kemajuan IT
	di Indonesia.
</p>
<br>
<h2>Kapan CompFest diadakan? Apa saja konten acaranya?</h2>
<p class="buh" >
CompFest sudah mengadakan beberapa rangkaian acaranya yaitu <a href="http://compfest.web.id/roadshow">roadshow </a>dan	<a href="http://compfest.web.id/competition">kompetisi</a>. Roadshow telah diadakan pada bulan Mei 2013 dan pada bulan itu juga kompetisi dimulai hingga
	saat ini. <a href="http://compfest.web.id/roadshow"></a>
</p><br>
<p class="buh" >
